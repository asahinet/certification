public class CriterionCreationException extends Exception
{
    public CriterionCreationException()
    {
        super();
    }

    public CriterionCreationException(String s)
    {
        super(s);
    }

    public CriterionCreationException(String s, Throwable throwable)
    {
        super(s, throwable);
    }

    public CriterionCreationException(Throwable throwable)
    {
        super(throwable);
    }

}
